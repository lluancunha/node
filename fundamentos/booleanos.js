/**
 * estudo das variaveis booleanas (verdadeiro ou falso)
 */

let sw = false
let lamp = true

console.log(typeof(sw))
console.log(sw)

console.log(typeof(lamp))
console.log(lamp)

// no Java Script é possivel usar 0 e 1 como booleano

sw = !1 // (falso)
lamp = !0 // (verdadeiro)

console.log(typeof(sw))
console.log(sw)

console.log(sw || "lampada apagada") // se a condiçao for verdadeira ira imprimir ""TRUE" se for falsa ira imprimir  "lampada apagada"

 console.log(typeof(lamp))
 console.log(lamp)